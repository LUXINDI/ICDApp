public class APNSingleResult {
    public String DiagnosisTerm;
    public String General;
    public String Congenital;
    public String PerinatalNeonatal;
    public String Pregnancy;
    public String Childbirth;
    public String Postpartum;

    public String getDiagnosisTerm() {
        return DiagnosisTerm;
    }

    public void setDiagnosisTerm(String diagnosisTerm) {
        DiagnosisTerm = diagnosisTerm;
    }

    public String getGeneral() {
        return General;
    }

    public void setGeneral(String general) {
        General = general;
    }

    public String getCongenital() {
        return Congenital;
    }

    public void setCongenital(String congenital) {
        Congenital = congenital;
    }

    public String getPerinatalNeonatal() {
        return PerinatalNeonatal;
    }

    public void setPerinatalNeonatal(String perinatalNeonatal) {
        PerinatalNeonatal = perinatalNeonatal;
    }

    public String getPregnancy() {
        return Pregnancy;
    }

    public void setPregnancy(String pregnancy) {
        Pregnancy = pregnancy;
    }

    public String getChildbirth() {
        return Childbirth;
    }

    public void setChildbirth(String childbirth) {
        Childbirth = childbirth;
    }

    public String getPostpartum() {
        return Postpartum;
    }

    public void setPostpartum(String postpartum) {
        Postpartum = postpartum;
    }
}
